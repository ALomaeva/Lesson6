package com.lomaevaav;

public class Animal {
    public void jump(float j) {
        System.out.println("Животное подпрыгнуло на "+ j +" метров");
    }
    public void run(float r) {
        System.out.println("Животное пробежало " + r + " метров");
    }
    public void swim(float s) {
        System.out.println("Животное проплыло " + s + " метров");
    }
}